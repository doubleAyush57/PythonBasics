test_string = "hello world this is a           "

# .strip()
print(test_string, "test")
print(test_string.strip(), "test")

# len()
print(len(test_string))

# .lower()
print(test_string.lower())

# .upper()
print(test_string.upper())

# .split()
print(test_string.strip().split(" "))

# slice()
print(test_string[:])